# 리스트에 적용할 수 있는 기본 함수

numbers = [103, 52, 273, 32, 77]

print("numbers :",numbers)
print("최소값 min(numbers) :", min(numbers))
print("최대값 max(numbers) :", max(numbers))
print("모두 더한 값 sum(numbers) :",sum(numbers))
print()

